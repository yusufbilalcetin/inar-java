package week_01.live_class;

public class Subtraction {
    public static void main(String[] args) {
        // Beautify command on intelliJ is CMND + SHIFT + L
        System.out.println("tast");


        // Fractional numbers is by default stored into double
        // integer can not store fractional values
        System.out.println("Integer division: " + 9/2);

        //If double and integers are calculated together, the output will always be double
        System.out.println("Math division: " + 9.0/2);

        // 16 bit 32 bit => 32 bit
        // String + double + integer = ? String
        System.out.println("Math division: " + 9.0 + 2);

        // String + double = ? string
        System.out.println("Math division: " + 9.0);

        // double + integer = ? double
        System.out.println(9.0 + 2);

    }
}
